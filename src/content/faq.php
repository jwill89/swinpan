                <p id="whatis" style="font-weight:bold;">What is SWINPAN?</p>
                <p>The Southwestern Indiana Partnership Action Network (SWINPAN) is a database of local groups and
                    organizations designed to connect them with each other, and to connect people who want to volunteer
                    and get active within their community to groups which match their interests.</p>
                <p id="political" style="font-weight:bold;">Is this a political group?</p>
                <p>Absolutely not. SWINPAN covers many issues as possible interest "tags" a group can select, and while
                    political action is one of them, SWINPAN a tool, and therefore takes no stance on anything political.
                    Our only goal is helping to bring together local groups and creating a repository of those groups to
                    make it easier to find each other, and for members of the community to find you.</p>
                <p id="benefits" style="font-weight:bold;">What are the benefits of association?</p>
                <p>The benefits of submitting your organization will be more exposure, making it easier for other groups
                    to find you and potential work together, which means more volunteers, and eventually a unified
                    community calendar of events. Additionally, we understand some organizations and groups may not have
                    a full technology stack for communication or outreach. We can provide email accounts, sub-domains for
                    websites, and other help on an as-needed basis.</p>
                <p id="apply" style="font-weight:bold;">How do I list my group in the database?</p>
                <p>It's easy, simply click "Submit Your Group" under the "Groups" menu, or <a href="index.php?p=add">click here</a>. Your group
                    will be placed in a queue for review and, upon approval, you will receive an email congratulating
                    you and reminding you of what we offer. If you ever need to change any of this information, you can
                    email <a href="mailto:info@swinpan.org">info@swinpan.org</a>.</p>
