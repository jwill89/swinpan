                <h3>What We Are Not</h3>

                <p><strong>We are not in any way a partisan or political group</strong>. We do not affiliate with any political party, however political entities may apply to be a partnered organization and work

                    with other groups as necessary. We do not have any official stances on any issues, on any politicians, on any laws. We are a database, a tool designed to

                    help local people find groups that fit their interests, and help local groups find each other to foster partnerships and awareness.</p>

