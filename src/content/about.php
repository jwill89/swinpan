                <h3>What We Are</h3>

                <p>The <strong>Southwestern Indiana Partnership Action Network</strong> is a simple idea. We are a hub, a database for groups and organizations in Southwestern Indiana to list

                    themselves and find other groups to coordinate with, fostering an atmosphere of community, helping Hoosiers in the region by getting involved directly

                    at the community level across a wide variety of interests, issues, and causes. We are also here to provide a gateway for any resident of Southwestern

                    Indiana looking to turn their passion into real action.</p>

